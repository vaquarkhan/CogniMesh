"use strict";

/**
 * AgentCore Runtime (Strands) deploy target.
 * Generates a standalone Strands + BedrockAgentCoreApp project from a CogniMesh
 * agent manifest. Optionally calls CreateAgentRuntime if enabled via env vars.
 */

function runtimeEnabled() {
  return (
    process.env.AGENTCORE_RUNTIME_ENABLED === "true" &&
    Boolean(process.env.AGENTCORE_RUNTIME_ROLE_ARN?.trim()) &&
    Boolean(process.env.AGENTCORE_ECR_IMAGE_URI?.trim())
  );
}

function resolveModel(manifest) {
  const specModel = manifest?.spec?.foundationModels?.[0]?.modelId;
  return specModel || process.env.AWS_BEDROCK_AGENT_MODEL || "amazon.nova-lite-v1:0";
}

function renderToolStubs(manifest) {
  const tools = manifest?.spec?.tools || [];
  if (!tools.length) return "# No tools defined — add @tool functions here\n";
  return tools.map((t) => {
    const name = (t.label || t.functionName || "custom_tool").replace(/[^a-zA-Z0-9_]/g, "_").toLowerCase();
    const desc = t.description || `Tool: ${t.label || name}`;
    return `@tool\ndef ${name}(query: str) -> str:\n    """${desc}"""\n    # TODO: implement\n    return f"Result for {query}"\n`;
  }).join("\n");
}

function generateAgentCoreProject(manifest) {
  const agentName = manifest?.metadata?.name || "my-agent";
  const description = manifest?.metadata?.description || `CogniMesh AgentCore agent: ${agentName}`;
  const model = resolveModel(manifest);
  const region = process.env.AWS_REGION || "us-east-1";
  const toolStubs = renderToolStubs(manifest);

  const agentPy = `"""
${agentName} — Generated by CogniMesh AgentCore Runtime (Strands).
Deploy: ./deploy.sh or docker build + push to ECR.
"""
import os
from strands import Agent, tool
from strands.models.bedrock import BedrockModel

# Model configuration
MODEL_ID = os.environ.get("BEDROCK_MODEL_ID", "${model}")
REGION = os.environ.get("AWS_REGION", "${region}")

model = BedrockModel(model_id=MODEL_ID, region_name=REGION)

# --- Tools ---
${toolStubs}
# --- Agent ---
agent = Agent(
    model=model,
    system_prompt="""${description}
Follow CogniMesh governance and safety policies.""",
    tools=[${(manifest?.spec?.tools || []).map((t) => (t.label || t.functionName || "custom_tool").replace(/[^a-zA-Z0-9_]/g, "_").toLowerCase()).join(", ")}],
)

if __name__ == "__main__":
    print(f"Agent '${agentName}' ready (model={MODEL_ID}, region={REGION})")
    while True:
        user_input = input("\\nYou: ")
        if user_input.lower() in ("exit", "quit"):
            break
        response = agent(user_input)
        print(f"\\nAgent: {response}")
`;

  const requirements = `strands-agents>=0.1.0
strands-agents-tools>=0.1.0
boto3>=1.34.0
bedrock-agentcore>=0.1.0
python-dotenv>=1.0.0
`;

  const dockerfile = `FROM python:3.13-slim

WORKDIR /app
COPY requirements.txt .
RUN pip install --no-cache-dir -r requirements.txt
COPY . .

ENV PYTHONUNBUFFERED=1
CMD ["python", "agent.py"]
`;

  const deployScript = `#!/bin/bash
set -e

AGENT_NAME="${agentName}"
AWS_REGION="\${AWS_REGION:-${region}}"
ECR_REPO="\${AGENTCORE_ECR_REPO:-cognimesh-agentcore-\${AGENT_NAME}}"
ACCOUNT_ID=$(aws sts get-caller-identity --query Account --output text)
ECR_URI="\${ACCOUNT_ID}.dkr.ecr.\${AWS_REGION}.amazonaws.com/\${ECR_REPO}"

echo "Building AgentCore Runtime: \${AGENT_NAME}"
echo "ECR: \${ECR_URI}"

# Create ECR repo if not exists
aws ecr describe-repositories --repository-names "\${ECR_REPO}" 2>/dev/null || \\
  aws ecr create-repository --repository-name "\${ECR_REPO}"

# Login + build + push
aws ecr get-login-password --region "\${AWS_REGION}" | docker login --username AWS --password-stdin "\${ECR_URI}"
docker build --platform linux/arm64 -t "\${ECR_REPO}:latest" .
docker tag "\${ECR_REPO}:latest" "\${ECR_URI}:latest"
docker push "\${ECR_URI}:latest"

echo ""
echo "Image pushed: \${ECR_URI}:latest"
echo ""
echo "To create the AgentCore Runtime:"
echo "  aws bedrock-agentcore-control create-agent-runtime \\\\"
echo "    --agent-runtime-name \${AGENT_NAME} \\\\"
echo "    --agent-runtime-artifact '{\"containerImage\": {\"uri\": \"\${ECR_URI}:latest\"}}' \\\\"
echo "    --role-arn \${AGENTCORE_RUNTIME_ROLE_ARN}"
`;

  const envExample = `# AgentCore Runtime configuration
BEDROCK_MODEL_ID=${model}
AWS_REGION=${region}
# AGENTCORE_RUNTIME_ROLE_ARN=arn:aws:iam::ACCOUNT:role/agentcore-runtime-role
# AGENTCORE_ECR_REPO=cognimesh-agentcore-${agentName}
`;

  const readme = `# ${agentName} — AgentCore Runtime (Strands)

Generated by CogniMesh portal.

## Quick Start

\`\`\`bash
pip install -r requirements.txt
python agent.py
\`\`\`

## Deploy to AWS

\`\`\`bash
chmod +x deploy.sh
./deploy.sh
\`\`\`

This builds a Docker image (linux/arm64), pushes to ECR, and provides the
\`aws bedrock-agentcore-control create-agent-runtime\` command.

## Files

| File | Purpose |
|------|---------|
| agent.py | Strands Agent + BedrockModel + tools |
| requirements.txt | Python dependencies |
| Dockerfile | Container build (arm64) |
| deploy.sh | ECR push + runtime create |
| .env.example | Environment variables |

## Model

Default: \`${model}\`. Override via \`BEDROCK_MODEL_ID\` env var.
`;

  return {
    "agent.py": agentPy,
    "requirements.txt": requirements,
    "Dockerfile": dockerfile,
    "deploy.sh": deployScript,
    ".env.example": envExample,
    "README.md": readme,
  };
}

async function deployAgentCoreRuntime(manifest, { userEmail } = {}) {
  const project = generateAgentCoreProject(manifest);
  const agentName = manifest?.metadata?.name || "my-agent";
  const model = resolveModel(manifest);
  const region = process.env.AWS_REGION || "us-east-1";

  if (!runtimeEnabled()) {
    return {
      deployed: false,
      generated: true,
      simulated: true,
      target: "agentcore-runtime",
      agentName,
      model,
      framework: "strands",
      region,
      project,
      projectFiles: Object.keys(project),
      reason: "AgentCore Runtime deploy not configured. Download the project and deploy manually.",
      nextSteps: [
        "Download the generated project (.zip)",
        "pip install -r requirements.txt && python agent.py (local test)",
        "chmod +x deploy.sh && ./deploy.sh (build + push to ECR)",
        "Set AGENTCORE_RUNTIME_ENABLED=true + AGENTCORE_RUNTIME_ROLE_ARN + AGENTCORE_ECR_IMAGE_URI on the API to enable live deploy",
      ],
      message: "AgentCore Runtime project generated. Download and deploy with deploy.sh.",
    };
  }

  // Live deploy path
  try {
    const {
      BedrockAgentCoreControlClient,
      CreateAgentRuntimeCommand,
    } = require("@aws-sdk/client-bedrock-agentcore-control");

    const client = new BedrockAgentCoreControlClient({ region });
    const imageUri = process.env.AGENTCORE_ECR_IMAGE_URI;
    const roleArn = process.env.AGENTCORE_RUNTIME_ROLE_ARN;

    const result = await client.send(new CreateAgentRuntimeCommand({
      agentRuntimeName: agentName,
      agentRuntimeArtifact: { containerImage: { uri: imageUri } },
      roleArn,
    }));

    return {
      deployed: true,
      generated: true,
      simulated: false,
      target: "agentcore-runtime",
      agentName,
      model,
      framework: "strands",
      region,
      agentRuntimeArn: result.agentRuntimeArn,
      agentRuntimeId: result.agentRuntimeId,
      project,
      projectFiles: Object.keys(project),
      consoleUrl: `https://${region}.console.aws.amazon.com/bedrock/home?region=${region}#/agent-runtimes`,
      deployedBy: userEmail || null,
      message: `AgentCore Runtime created: ${result.agentRuntimeId || agentName}`,
    };
  } catch (err) {
    return {
      deployed: false,
      generated: true,
      simulated: false,
      target: "agentcore-runtime",
      agentName,
      model,
      framework: "strands",
      region,
      project,
      projectFiles: Object.keys(project),
      errors: [err.message],
      message: `Runtime creation failed: ${err.message}. Project still available for manual deploy.`,
      nextSteps: [
        "Download the generated project and run deploy.sh manually",
        `Error: ${err.message}`,
      ],
    };
  }
}

module.exports = { generateAgentCoreProject, deployAgentCoreRuntime, runtimeEnabled };
